<p>Time management is the act or process of planning and exercising conscious control over the amount of time spent on specific activities, especially to increase effectiveness, efficiency or productivity. Time management may be aided by a range of skills, tools, and techniques used to manage time when accomplishing specific tasks, projects and goals complying with a due date. Initially, time management referred to just business or work activities, but eventually the term broadened to include personal activities as well. A time management system is a designed combination of processes, tools, techniques, and methods. Time management is usually a necessity in any project development as it determines the project completion time and scope.<p>
<p>Time management is often considered necessary because:</p>
<ol>
  	<li>Available time is limited.</li>
  	<li>Time cannot be stored: if unused it is lost forever.</li>
	<li>One's goals are usually multiple, sometimes conflict, and not all goals are of equal priority.</li>
	<li>Goals cannot be accomplished without the application of effort, which requires the use of time.</li>
</ol>

<p>All tasks are evaluated using the criteria important/unimportant and urgent/not urgent and put in according quadrants. Tasks in unimportant/not urgent quadrants are dropped, tasks in important/urgent quadrants are done immediately and personally, tasks in unimportant/urgent quadrants are delegated and tasks in important/not urgent quadrants get an end date and are done personally.</p>